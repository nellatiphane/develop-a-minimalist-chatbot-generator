// Define the chatbot's knowledge base (Q&A pairs)
$knowledgeBase = [
    "hello" => "Hi! How can I help you?",
    "hi" => "Hey! What's on your mind?",
    "how are you" => "I'm doing great, thanks! How about you?",
    "what is your name" => "I'm HNVX, your friendly chatbot!",
    "quit" => "See you later!",
    "exit" => "Goodbye!",
    "default" => "I'm not sure I understand. Can you please rephrase?"
];

// Define the chatbot's personality traits (optional)
$personalityTraits = [
    "tone" => "friendly",
    "emotions" => ["happy", "curious"]
];

// Generate the chatbot's script
$chatbotScript = "<?php\n";
$chatbotScript .= "// Chatbot script generated by hnvx_develop_a_minim.php\n";
$chatbotScript .= "// Knowledge base:\n";
foreach ($knowledgeBase as $input => $response) {
    $chatbotScript .= "if (strtolower(\$input) == '$input') {\n";
    $chatbotScript .= "    echo '$response';\n";
    $chatbotScript .= "} elseif (\$input == '') {\n";
    $chatbotScript .= "    echo 'Please enter a message.';\n";
    $chatbotScript .= "} else {\n";
    $chatbotScript .= "    echo '" . $knowledgeBase["default"] . "';\n";
    $chatbotScript .= "}\n";
}
$chatbotScript .= "?>";

// Save the chatbot script to a file
$file = fopen("hnvx_chatbot.php", "w");
fwrite($file, $chatbotScript);
fclose($file);

// Output the chatbot script to the console
echo $chatbotScript;

